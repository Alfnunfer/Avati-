    # Muestra la matriz 8x8 en el display MAX7219
    def Mostrar(self, device, Dibujo):
        #print(Dibujo)
        matrix = self.ojos[Dibujo]
        device.clear() #apago los led
        with canvas(device) as draw:
            for row in range(8):
                for col in range(8):
                    if matrix[row][col] == 1:
                        draw.point((col, row), fill="white")
